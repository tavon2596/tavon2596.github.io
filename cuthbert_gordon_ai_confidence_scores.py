def truncate_output(raw_text):
    """
    Cut off the model's output at the first sign it's going off-script —
    generating new questions, restarting the format, or adding markdown headers.
    """
    cutoffs = ['\nQ:', '\n---', '\n**', '\nInstruction', '\nNow answer', '--- END']
    for marker in cutoffs:
        idx = raw_text.find(marker)
        if idx != -1:
            raw_text = raw_text[:idx]
    return raw_text.strip()


def clean_answer(text):
    """
    Strip common model artifacts from the beginning of an extracted answer:
    - Markdown headers like '### AI:' or '## Solution:'
    - Role prefixes like 'AI:' or 'Assistant:'
    - Leading blank lines
    Falls back to None if nothing real is left after cleaning.
    """
    if not text:
        return None
    # Remove markdown header lines at the start (e.g. "### AI:\n\n")
    text = re.sub(r'^#+\s*\w*:?\s*\n+', '', text, flags=re.MULTILINE).strip()
    # Remove role prefixes at the start (e.g. "AI:\n" or "Assistant:\n")
    text = re.sub(r'^(AI|Assistant|Response)\s*:\s*\n+', '', text, flags=re.IGNORECASE).strip()
    # If what's left starts with CONFIDENCE: it means ANSWER was blank — discard it
    if re.match(r'^CONFIDENCE:', text, re.IGNORECASE):
        return None
    # If nothing meaningful remains, return None
    return text if len(text.strip()) > 3 else None


def parse_response(raw_text, condition):
    """
    Extract REASONING (condition 2 only), ANSWER, and CONFIDENCE.
    Truncates runaway output first, then cleans and validates each field.
    """
    raw_text = truncate_output(raw_text)
    result = {'reasoning': None, 'answer': None, 'confidence': None, 'parse_ok': False}

    if condition == 2:
        r = re.search(r'REASONING:\s*(.+?)(?=ANSWER:|$)', raw_text, re.IGNORECASE | re.DOTALL)
        val = r.group(1).strip() if r else None
        result['reasoning'] = None if (val and '<' in val) else val

    a = re.search(r'ANSWER:\s*(.+?)(?=CONFIDENCE:|$)', raw_text, re.IGNORECASE | re.DOTALL)
    val = a.group(1).strip() if a else None
    # Skip placeholders like "<your answer in one sentence>"
    if val and '<' in val:
        val = None
    result['answer'] = clean_answer(val)

    c = re.search(r'CONFIDENCE:\s*(\d{1,3})', raw_text, re.IGNORECASE)
    if c:
        result['confidence'] = min(max(int(c.group(1)), 0), 100)

    result['parse_ok'] = (result['answer'] is not None and result['confidence'] is not None)
    return result


print('parse_response(), truncate_output(), and clean_answer() defined.')

def grade_answer(model_answer, correct_answer):
    """
    Keyword-overlap grading (Week 3 of the spec timeline).

    Strips stopwords, then checks what fraction of the reference answer's
    content words appear in the model's answer.
    Threshold: >= 50% keyword overlap = CORRECT.

    This is intentionally simple and transparent. A more robust approach
    can replace this function later
    without changing any other code.
    """
    if not model_answer or not correct_answer:
        return False

    STOPWORDS = {
        'a','an','the','is','are','was','were','be','been','being',
        'it','in','on','of','to','and','or','but','that','this',
        'for','with','not','no','by','as','at','from','have','has'
    }

    def keywords(text):
        tokens = re.sub(r'[^a-z0-9 ]', '', text.lower()).split()
        return {t for t in tokens if t not in STOPWORDS and len(t) > 1}

    ref_kw   = keywords(correct_answer)
    model_kw = keywords(model_answer)
    if not ref_kw:
        return False
    overlap = len(ref_kw & model_kw) / len(ref_kw)
    return overlap >= 0.5


