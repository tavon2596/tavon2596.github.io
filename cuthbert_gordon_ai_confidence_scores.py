"""---
## Section 6 — Inference and Parsing Utilities

- `run_inference()` — routes to local HF model (Stages 1 & 2) or OpenAI API (Stage 3)
- `parse_response()` — extracts `REASONING`, `ANSWER`, `CONFIDENCE` from the raw output using regex
- `grade_answer()` — checks correctness via keyword overlap against the labeled correct answer
"""

def run_inference_hf(prompt, max_new_tokens=200):
    """Single-prompt HF inference. Used by the pilot (Section 7)."""
    inputs = tokenizer(prompt, return_tensors='pt').to(model.device)
    in_len = inputs['input_ids'].shape[1]
    with torch.no_grad():
        out_ids = model.generate(
            **inputs,
            max_new_tokens=max_new_tokens,
            do_sample=False,
            temperature=1.0,
            pad_token_id=tokenizer.eos_token_id,
        )
    new_tokens = out_ids[0][in_len:]
    return tokenizer.decode(new_tokens, skip_special_tokens=True).strip()


def run_inference_batch_hf(prompts, max_new_tokens=200):
    """
    Batched HF inference runs multiple prompts through the GPU simultaneously.
    Instead of sending one question at a time and waiting, we send BATCH_SIZE
    questions at once. The GPU processes them in parallel.
    """
    tokenizer.padding_side = 'left'
    if tokenizer.pad_token is None:
        tokenizer.pad_token = tokenizer.eos_token

    encoded = tokenizer(
        prompts,
        return_tensors='pt',
        padding=True,
        truncation=True,
        max_length=1024,
    ).to(model.device)

    input_len = encoded['input_ids'].shape[1]
    with torch.no_grad():
        out_ids = model.generate(
            **encoded,
            max_new_tokens=max_new_tokens,
            do_sample=False,
            temperature=1.0,
            pad_token_id=tokenizer.eos_token_id,
        )

    return [
        tokenizer.decode(out[input_len:], skip_special_tokens=True).strip()
        for out in out_ids
    ]


def run_inference_api(prompt, max_tokens=250):
    """OpenAI API inference (Stage 3 — proprietary model)."""
    import openai
    response = openai.chat.completions.create(
        model=MODEL_ID,
        messages=[{'role': 'user', 'content': prompt}],
        max_tokens=max_tokens,
        temperature=0,
    )
    return response.choices[0].message.content.strip()


def run_inference(prompt, max_new_tokens=200):
    """Single-prompt router — used by the pilot. Section 8 uses run_batch."""
    if MODEL_STAGE in [1, 2]:
        return run_inference_hf(prompt, max_new_tokens)
    else:
        return run_inference_api(prompt, max_tokens=max_new_tokens)


def run_batch(prompts, max_new_tokens=200):
    """Batch router — used by Section 8 full experiment."""
    if MODEL_STAGE in [1, 2]:
        return run_inference_batch_hf(prompts, max_new_tokens)
    else:
        # OpenAI has no batch endpoint; sequential but still benefits from reduced tokens
        return [run_inference_api(p, max_tokens=max_new_tokens) for p in prompts]


