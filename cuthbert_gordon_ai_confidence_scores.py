def extract_qa(entry):
    """
    Extract from a TruthfulQA multiple_choice entry:
      question       — the question string
      correct_answer — best labeled-correct answer  (mc1_targets label == 1)
      wrong_answer   — most plausible wrong answer  (mc1_targets label == 0, index 0)

    Both correct and wrong answers are stored so we can verify the model is
    being graded against the actual correct answer and not a coincidental match.
    """
    choices = entry['mc1_targets']['choices']
    labels  = entry['mc1_targets']['labels']
    correct = [c for c, l in zip(choices, labels) if l == 1]
    wrong   = [c for c, l in zip(choices, labels) if l == 0]
    return {
        'question'       : entry['question'],
        'correct_answer' : correct[0] if correct else 'N/A',
        'wrong_answer'   : wrong[0]   if wrong   else 'N/A',
    }

